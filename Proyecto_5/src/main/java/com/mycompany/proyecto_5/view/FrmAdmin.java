    private void btnAgregarFuncionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarFuncionActionPerformed
        // TODO add your handling code here:
        Object peliculaSel = cmbPeliculas.getSelectedItem();
Object salaSel = cmbSalas.getSelectedItem();
String fechaHora = txtFechaHora.getText().trim();

if (peliculaSel == null || salaSel == null || fechaHora.isEmpty()) {
    javax.swing.JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios.");
    return;
}

try {
    int idPelicula = Integer.parseInt(peliculaSel.toString().split(" - ")[0].trim());
    int idSala = Integer.parseInt(salaSel.toString().split(" - ")[0].trim());
    
    FuncionDAO dao = new FuncionDAO();
    if (dao.agregarFuncion(idSala, idPelicula, fechaHora)) {
        javax.swing.JOptionPane.showMessageDialog(this, "Función agregada exitosamente.");
        txtFechaHora.setText("");
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Error al agregar función.");
    }
} catch (Exception e) {
    javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
}
    }//GEN-LAST:event_btnAgregarFuncionActionPerformed

    private void btnAgregarPeliculaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarPeliculaActionPerformed
        // TODO add your handling code here:
        String titulo = txtTitulo.getText().trim();
String duracionStr = txtDuracion.getText().trim();
String clasificacion = (String) cmbClasificacion.getSelectedItem();

if (titulo.isEmpty() || duracionStr.isEmpty() || clasificacion == null) {
    javax.swing.JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios.");
    return;
}

try {
    int duracion = Integer.parseInt(duracionStr);
    PeliculaDAO dao = new PeliculaDAO();
    if (dao.agregarPelicula(titulo, duracion, clasificacion)) {
        javax.swing.JOptionPane.showMessageDialog(this, "Película agregada exitosamente.");
        txtTitulo.setText("");
        txtDuracion.setText("");
        cargarComboPeliculas();
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Error al agregar película.");
    }
} catch (NumberFormatException e) {
    javax.swing.JOptionPane.showMessageDialog(this, "Duración debe ser un número.");
}
    }//GEN-LAST:event_btnAgregarPeliculaActionPerformed

