    private void cargarComboPeliculas() {
    cmbPeliculas.removeAllItems();
    PeliculaDAO dao = new PeliculaDAO();
    for (String p : dao.listarPeliculas()) {
        cmbPeliculas.addItem(p);
    }
}

    private void cargarComboSalas() {
    cmbSalas.removeAllItems();
    SalaDAO dao = new SalaDAO();
    for (String s : dao.listarSalas()) {
        cmbSalas.addItem(s);
    }
} 

